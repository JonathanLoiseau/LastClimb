package com.last_climb.climb.model;

public class IdContainer {
	private Long siteId;
	private Long secteurId;
	private Long voieId;

	public Long getSiteId() {
		return siteId;
	}

	public void setSiteId(Long siteId) {
		this.siteId = siteId;
	}

	public Long getSecteurId() {
		return secteurId;
	}

	public void setSecteurId(Long secteurId) {
		this.secteurId = secteurId;
	}

	public Long getVoieId() {
		return voieId;
	}

	public void setVoieId(Long voieId) {
		this.voieId = voieId;
	}

}
